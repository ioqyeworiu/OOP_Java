public class MaTruyVan {
    private String gioiTinh, sizeAo;

    public MaTruyVan(String gioiTinh, String sizeAo) {
        this.gioiTinh = gioiTinh;
        this.sizeAo = sizeAo;
    }

    public String getGioiTinh() {
        return gioiTinh;
    }

    public String getSizeAo() {
        return sizeAo;
    }
}
